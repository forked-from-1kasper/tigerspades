static uint8_t revlookup(uint32_t codepoint, uint32_t * encoding) {
    for (size_t i = 0; i < 256; i++)
        if (encoding[i] == codepoint)
            return i;

    return encoding[0x3F];
}

uint8_t encode(uint8_t * dest, uint32_t codepoint, Codepage codepage) {
    switch (codepage) {
        case UTF8: {
            if (codepoint <= 0x7F) {
                dest[0] = codepoint;
                return 1;
            } else if (codepoint <= 0x7FF) {
                dest[0] = (codepoint >> 6)         | 0xC0;
                dest[1] = (codepoint & 0x3F)       | 0x80;
                return 2;
            } else if (codepoint <= 0xFFFF) {
                dest[0] = (codepoint >> 12)        | 0xE0;
                dest[1] = (codepoint >> 6 & 0x3F)  | 0x80;
                dest[2] = (codepoint & 0x3F)       | 0x80;
                return 3;
            } else if (codepoint <= 0x10FFFF) {
                dest[0] = (codepoint >> 18)        | 0xF0;
                dest[1] = (codepoint >> 12 & 0x3F) | 0x80;
                dest[2] = (codepoint >> 6 & 0x3F)  | 0x80;
                dest[3] = (codepoint & 0x3F)       | 0x80;
                return 4;
            } else return 0;
        }

        case CP437:  dest[0] = revlookup(codepoint, cp437);  return 1;
        case CP1252: dest[0] = revlookup(codepoint, cp1252); return 1;
        default:     return 0;
    }
}

uint8_t decode(uint8_t * bytes, uint32_t * outptr, Codepage codepage) {
    *outptr = 0;

    switch (codepage) {
        case UTF8: {
            if (OCT1(bytes[0])) {
                *outptr = bytes[0];
                return 1;
            } else if (OCT2(bytes[0])) {
                *outptr |= (bytes[0] & 0x1F) << 6;
                *outptr |= (bytes[1] & 0x3F);
                return 2;
            } else if (OCT3(bytes[0])) {
                *outptr |= (bytes[0] & 0x0F) << 12;
                *outptr |= (bytes[1] & 0x3F) << 6;
                *outptr |= (bytes[2] & 0x3F);
                return 3;
            } else if (OCT4(bytes[0])) {
                *outptr |= (bytes[0] & 0x07) << 18;
                *outptr |= (bytes[1] & 0x3F) << 12;
                *outptr |= (bytes[2] & 0x3F) << 6;
                *outptr |= (bytes[3] & 0x3F);
                return 4;
            } else { *outptr = 0xFFFD; return 1; }
        }

        case CP437:  *outptr = cp437[*bytes];  return 1;
        case CP1252: *outptr = cp1252[*bytes]; return 1;
        default:     return 0;
    }
}

void reencode(uint8_t * dest, const uint8_t * src, Codepage inpage, Codepage outpage) {
    while (*src) {
        uint32_t codepoint; src += decode(src, &codepoint, inpage);
        dest += encode(dest, codepoint, outpage);
    }

    *dest = 0;
}

void encodeMagic(uint8_t * dest, const uint8_t * src, size_t insize, size_t outsize) {
    bool ascii = true;

    for (size_t i = 0; i < insize; i++) {
        if (!OCT1(src[i])) {
            ascii = false; break;
        }
    }

    if (ascii) strncpy(dest, src, outsize);
    else {
        dest[0] = 0xFF;
        strncpy(dest, src, outsize - 1);
    }
}

void decodeMagic(uint8_t * dest, const uint8_t * src, size_t size) {
    if (src[0] == 0xFF) {
        strncpy(dest, src + 1, size);
        dest[size - 1] = 0;
    } else reencode(dest, src, CP437, UTF8);
}